def affine_bn_relu_forward(x, w, b, gamma, beta, bn_param, use_bn: bool, use_drop:bool, dropout_param):
  """Helper function for Affine-Batchnorm-Relu layer forward pass with dropout
  """
  bn_cache = None
  do_cache = None
  #Affine forward step
  out, af_cache = affine_forward(x, w, b)
  #Check if batch normalization is used
  if use_bn:
    #Apply batch normalization
    out, bn_cache = batchnorm_forward(out, gamma, beta, bn_param)
  #Apply Relu
  out, rl_cache = relu_forward(out)
  #Apply dropout
  if use_drop:
    out, do_cache = dropout_forward(out, dropout_param)

  return out, af_cache, bn_cache, rl_cache, do_cache


def affine_bn_relu_backward(dout, af_cache, bn_cache, rl_cache, do_cache, use_bn: bool, use_drop:bool):
  """Helper function for Affine-Batchnorm-Relu layer backward pass with dropout
  """
  dgamma, dbeta = None, None
  if use_drop:
    #Dropout backward
    dout = dropout_backward(dout, do_cache)
  #Relu backward
  dout = relu_backward(dout, rl_cache)
  #Check if batch normalization is used
  if use_bn:
    #Batch norm backward
    dout, dgamma, dbeta = batchnorm_backward(dout, bn_cache)
  #Affine backward
  dx, dW, db = affine_backward(dout, af_cache)

  return dx, dW, db, dgamma, dbeta

